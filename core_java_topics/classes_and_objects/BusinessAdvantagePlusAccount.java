package core_java_topics.classes_and_objects;

public class BusinessAdvantagePlusAccount {
    private long bapa_acc_num;
    private String bapa_acc_holder_name;
    private String bapa_branch_name;
    private String bapa_city_name;
    private double bapa_acc_balance;

    public long getBapa_acc_num() {
        return bapa_acc_num;
    }

    public void setBapa_acc_num(long bapa_acc_num) {
        this.bapa_acc_num = bapa_acc_num;
    }

    public String getBapa_acc_holder_name() {
        return bapa_acc_holder_name;
    }

    public void setBapa_acc_holder_name(String bapa_acc_holder_name) {
        this.bapa_acc_holder_name = bapa_acc_holder_name;
    }

    public String getBapa_acc_branch_name() {
        return bapa_branch_name;
    }

    public void setBapa_acc_branch_name(String bapa_branch_name) {
        this.bapa_branch_name = bapa_branch_name;
    }

    public String getBapa_acc_city_name() {
        return bapa_city_name;
    }

    public void setBapa_acc_city_name(String bapa_city_name) {
        this.bapa_city_name = bapa_city_name;
    }

    public double getBapa_acc_balance() {
        return bapa_acc_balance;
    }

    public void setBapa_acc_balance(double bapa_acc_balance) {
        this.bapa_acc_balance = bapa_acc_balance;
    }
}

